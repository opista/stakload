import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const packageRoot = path.resolve(__dirname, "..");
const protoPath = path.join(packageRoot, "vendor", "igdbapi.proto");
const outputDir = path.join(packageRoot, "src", "generated");
const outputPath = path.join(outputDir, "resources.ts");

const resourceOverrides = {
  Search: "search",
  TestDummy: "test_dummies",
};

const irregularPlurals = {
  specie: "species",
};

const toPascalParts = (value) => value.match(/[A-Z]+(?:[a-z0-9]+|(?=[A-Z]|$))/g) ?? [value];

const toSnakeCase = (value) =>
  value
    .replace(/([a-z0-9])([A-Z])/g, "$1_$2")
    .replace(/([A-Z]+)([A-Z][a-z])/g, "$1_$2")
    .toLowerCase();

const pluralizePart = (value) => {
  const normalized = value.toLowerCase();

  if (normalized in irregularPlurals) {
    return irregularPlurals[normalized];
  }

  if (/[^aeiou]y$/i.test(value)) {
    return `${value.slice(0, -1)}ies`;
  }

  if (/(s|x|z|ch|sh)$/i.test(value)) {
    return `${value}es`;
  }

  return `${value}s`;
};

const toResourceName = (entityMessage) => {
  const override = resourceOverrides[entityMessage];

  if (override) {
    return override;
  }

  const parts = toPascalParts(entityMessage);
  const lastPart = parts.at(-1);
  const pluralTargetIndex = lastPart && /^V\d+$/i.test(lastPart) ? parts.length - 2 : parts.length - 1;

  if (pluralTargetIndex >= 0) {
    parts[pluralTargetIndex] = pluralizePart(parts[pluralTargetIndex]);
  }

  return parts.map((part) => toSnakeCase(part)).join("_");
};

const proto = await readFile(protoPath, "utf8");

const resourceDefinitions = [...proto.matchAll(/message\s+(\w+)Result\s*\{\s*repeated\s+(\w+)\s+(\w+)\s*=\s*1;/g)]
  .map(([, resultBaseName, entityMessage, protoField]) => ({
    entityMessage,
    protoField,
    resource: toResourceName(entityMessage),
    resultMessage: `${resultBaseName}Result`,
  }))
  .sort((left, right) => left.resource.localeCompare(right.resource));

const generated = `// This file is auto-generated by scripts/generate-resources.mjs.
// Do not edit it manually. Update vendor/igdbapi.proto and rerun \`pnpm --dir packages/igdb-vendor generate\`.

export const IGDB_RESOURCE_DEFINITIONS = ${JSON.stringify(resourceDefinitions, null, 2)} as const;

export const IGDB_RESOURCES = IGDB_RESOURCE_DEFINITIONS.map((definition) => definition.resource);
`;

await mkdir(outputDir, { recursive: true });
await writeFile(outputPath, generated);
